package Calculadora.dominio;

public class MathOperation {
    public double addNumbers(double num1, double num2) {
        return num1 + num2;
    }

    public double subtractNumbers(double num1, double num2) {
        return num1 - num2;
    }

    public double multiplicationNumbers(double num1, double num2) {
        return num1 * num2;
    }

    public double divisionNumbers(double num1, double num2) {
        return num1 / num2;
    }
}
